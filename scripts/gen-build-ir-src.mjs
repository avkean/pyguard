#!/usr/bin/env node
// Regenerates lib/v5/build_ir_src.ts from lib/v5/build_ir.py.
//
// Embeds the IR compiler source as a raw string export so it can be piped
// into Pyodide in the browser without a separate network fetch.
//
// Usage: node scripts/gen-build-ir-src.mjs
//        (or: npm run gen:build-ir)

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');

const src = fs.readFileSync(
    path.join(root, 'lib/v5/build_ir.py'),
    'utf-8',
);

// Strip the __main__ self-test block — the browser path calls
// compile_to_json() directly via Pyodide's globals bridge.
const cutMarker = "# Self-test entry point";
const cut = src.indexOf(cutMarker);
const trimmed = cut >= 0 ? src.slice(0, cut).trimEnd() + '\n' : src;

// Escape for a TS template literal: backslashes, backticks, ${.
const escaped = trimmed
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');

const header = `// AUTO-GENERATED by scripts/gen-build-ir-src.mjs.
// Do not edit by hand. Regenerate with: node scripts/gen-build-ir-src.mjs
//
// Source: lib/v5/build_ir.py

export const BUILD_IR_SRC = \`${escaped}\`;
`;

const outPath = path.join(root, 'lib/v5/build_ir_src.ts');
fs.writeFileSync(outPath, header);
console.log(`wrote ${outPath} (${trimmed.length} chars)`);
