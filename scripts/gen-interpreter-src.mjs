#!/usr/bin/env node
// Regenerates lib/v5/interpreter_src.ts from lib/v5/runtime_interp.py.
//
// Embeds the interpreter source as a raw string export so the TS build
// can splice it into generated stubs without runtime filesystem access.
//
// Usage: node scripts/gen-interpreter-src.mjs
//        (or: npm run gen:interpreter)

import { execFileSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import zlib from 'node:zlib';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');

const src = fs.readFileSync(
    path.join(root, 'lib/v5/runtime_interp.py'),
    'utf-8',
);

// Strip the self-test __main__ block — not needed in stubs and pulls in
// build_ir which we never ship.
const cutMarker = "# --- self-test entry point";
const cut = src.indexOf(cutMarker);
const trimmedRaw = cut >= 0 ? src.slice(0, cut).trimEnd() + '\n' : src;

// Obfuscate the interpreter source via scripts/obfuscate_runtime.py.
// This applies AST-level transformations: identifier renaming (lIl1O0-
// style confusable names), XOR-encoded string literals, dead code
// insertion, and method order scrambling. The obfuscated source is
// functionally equivalent but dramatically harder for an attacker or
// LLM to understand.
//
// After obfuscation we minify (strip comments, blank lines).
// If obfuscation or minification fails, fall back gracefully.
let trimmed;
let bootKeyHex = '';   // v9: per-regen randomized export key
let bootFuncName = '';
try {
    // v9: run obfuscate_runtime.py once, capturing both streams. The
    // script emits obfuscated source to stdout AND a line like
    // `PYG_BOOT_KEY_HEX=...` to stderr so we can thread the same random
    // bytes into stage2 without re-running (which would desync the RNG).
    const { spawnSync } = await import('node:child_process');
    const res = spawnSync(
        'python3',
        [path.join(root, 'scripts/obfuscate_runtime.py')],
        { encoding: 'utf-8', timeout: 30000 },
    );
    if (res.status !== 0) {
        throw new Error(`obfuscate_runtime.py exit ${res.status}: ${res.stderr}`);
    }
    trimmed = res.stdout;
    if (res.stderr) {
        const m = /PYG_BOOT_KEY_HEX=([0-9a-f]+)/.exec(res.stderr);
        if (m) bootKeyHex = m[1];
        const fn = /PYG_BOOT_FUNC_NAME=([A-Za-z0-9_]+)/.exec(res.stderr);
        if (fn) bootFuncName = fn[1];
    }
    console.log('[gen-interpreter-src] obfuscated interpreter source');
} catch (err) {
    console.warn(
        `[gen-interpreter-src] obfuscator failed (${err.message}); ` +
        'falling back to minification only',
    );
    trimmed = trimmedRaw;
}
if (!bootKeyHex) {
    throw new Error('[gen-interpreter-src] FATAL: boot key not captured; stub would fail to boot');
}
if (!bootFuncName) {
    throw new Error('[gen-interpreter-src] FATAL: boot func name not captured; stub would fail to boot');
}
try {
    trimmed = execFileSync(
        'python3',
        [path.join(root, 'scripts/minify_py.py')],
        { input: trimmed, encoding: 'utf-8' },
    );
} catch (err) {
    console.warn(
        `[gen-interpreter-src] minifier skipped (${err.message}); ` +
        'embedding unminified source',
    );
}

// Compress the minified interpreter source with zlib so that the stub
// only carries ~30% of the bytes. At runtime stage2 will call
// zlib.decompress() + exec() to unpack it. Python's zlib module is in the
// stdlib, so this adds no runtime dependency. We use maximum compression
// because gen time is untimed but the resulting constant will be hit by
// every stub build.
const compressed = zlib.deflateRawSync(Buffer.from(trimmed, 'utf-8'), {
    level: zlib.constants.Z_BEST_COMPRESSION,
});
const compressedB64 = compressed.toString('base64');

const header = `// AUTO-GENERATED by scripts/gen-interpreter-src.mjs.
// Do not edit by hand. Regenerate with: node scripts/gen-interpreter-src.mjs
//
// Source: lib/v5/runtime_interp.py
//
// The interpreter source is minified (comments/docstrings/blank lines
// stripped), then raw-deflated and base64-encoded. At runtime stage2
// unpacks it via:
//
//     exec(__import__('zlib').decompress(
//         __import__('base64').b64decode(INTERPRETER_SRC_B64),
//         -15,
//     ).decode('utf-8'))
//
// where the \`-15\` wbits argument selects raw-deflate (no zlib header),
// matching deflateRawSync above. The payload is ~70% smaller than the
// raw source, which directly shrinks every generated stub.

export const INTERPRETER_SRC_B64 = ${JSON.stringify(compressedB64)};

// v9: the per-regen randomized bytes key under which the interpreter's
// module body registers the renamed \`_pg_boot\` function. Stage2 uses
// exactly these bytes to index \`interp_ns\` after running the module
// code object. No original name (like "_pg_boot") appears in the
// interpreter's globals dict at any point.
export const BOOT_KEY_BYTES = new Uint8Array([${
    Array.from(Buffer.from(bootKeyHex, 'hex')).join(', ')
}]);

// Current obfuscated identifier bound to _pg_boot in the generated
// interpreter module. Stage2 uses this per-build name after module init
// and never exposes a pre-boot provider object in interpreter globals.
export const BOOT_FUNC_NAME = ${JSON.stringify(bootFuncName)};

// Size metrics for the gen-time audit log.
export const INTERPRETER_SRC_RAW_BYTES = ${trimmed.length};
export const INTERPRETER_SRC_COMPRESSED_BYTES = ${compressed.length};
`;

const outPath = path.join(root, 'lib/v5/interpreter_src.ts');
fs.writeFileSync(outPath, header);
console.log(
    `wrote ${outPath}: raw ${trimmed.length} B -> ` +
    `deflate ${compressed.length} B -> base64 ${compressedB64.length} chars ` +
    `(ratio ${(compressed.length / trimmed.length * 100).toFixed(1)}%)`,
);
